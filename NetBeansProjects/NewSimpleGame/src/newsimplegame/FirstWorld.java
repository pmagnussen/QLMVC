    public void displayWorld(Graphics g) {

    // This is player rendered graphics.
    Graphics2D world = (Graphics2D) g;    // Graphical library to render shapes.

    for (int x = 0; x < (NewSimpleGame.WORLD_WIDTH / NewSimpleGame.WORLDPIXEL); x++) {
        for (int y = 0; y < (NewSimpleGame.WORLD_HEIGHT / NewSimpleGame.WORLDPIXEL); y++) {
            System.out.println("");

            if (worldArray[y][x] == 1) {
                world.setColor(Color.darkGray);
            }

            if (worldArray[y][x] == 0) { 
                world.setColor(Color.white);
                }
            
            if(worldArray[y][x] == 2) {
                world.setColor(Color.green);
            }
            
            world.drawRect(x*NewSimpleGame.WORLDPIXEL, y*NewSimpleGame.WORLDPIXEL, NewSimpleGame.WORLDPIXEL, NewSimpleGame.WORLDPIXEL);
            world.fillRect(x*NewSimpleGame.WORLDPIXEL, y*NewSimpleGame.WORLDPIXEL, NewSimpleGame.WORLDPIXEL, NewSimpleGame.WORLDPIXEL);
            }
        }
    }
